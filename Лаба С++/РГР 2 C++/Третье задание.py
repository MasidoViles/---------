list1 = [2, 5, 7, 9, 1] #Функция списка которая напоминает действие массива
max_number = max(list1) #Параметр max находит максимальное значение в листе
min_number = min(list1) #Параметр min находит минимальное значение в листе
print("Наибольшее число: ", max_number)#Вывод максимального числа
print("Наименьшее число: ", min_number)#Вывод минимального числа
